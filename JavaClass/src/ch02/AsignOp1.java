package ch02;
public class AsignOp1 {
	public static void main(String[] args) {
		int i1 = 10;
		System.out.println("i1 = " + i1);
		i1 += 4; // i1 = i1 + 4 => 10 + 4
		System.out.println("i1 = " + i1);
		i1 -= 4; // i1 = i1 - 4 => 14 - 4
		System.out.println("i1 = " + i1);
		i1 *= 4; // i1 = i1 * 4 = 10 * 4
		System.out.println("i1 = " + i1);
		i1 /= 4; // i1 = i1 / 4 => 40 / 4
		System.out.println("i1 = " + i1);
		i1 %= 4; // i1 = i1 % 4 => 10 % 4 
		System.out.println("i1 = " + i1);
	}
}
